package study.theory.functions

import kotlin.math.*

fun main() {

    println("O maior número é: " + max(5, 6))
    println("O menor número é: " + min(5, 6))
    println("A raiz quadrada de 25 é: " + sqrt(25.0))

    println(PI) // pi
    println(E) // Euler

    println("Arredondar 29.998 -> " + round(29.998))

}